def test_cli_plan_save_and_list(tmp_path):
    pkg = _make_violation_tree(tmp_path)
    result = runner.invoke(app, ["plan", str(pkg), "--save", "--json"])
    assert result.exit_code == 0
    plan = json.loads(result.stdout)
    plan_id = plan["id"]
    # Now list_cmd should see it.
    listing = runner.invoke(
        app, ["plan-list", "--project", str(pkg), "--json"])
    assert listing.exit_code == 0
    data = json.loads(listing.stdout)
    assert any(p["plan_id"] == plan_id for p in data["plans"])


