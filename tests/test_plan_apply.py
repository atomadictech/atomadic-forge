def test_apply_all_applyable_halts_on_failure(tmp_path):
    """With one applyable card pointing at a non-existent write_scope,
    apply_all_applyable returns halted_on='failed'."""
    plan: dict = {
        "schema_version": "atomadic-forge.agent_plan/v1",
        "id": "test-plan",
        "verdict": "FAIL", "goal": "ship", "mode": "improve",
        "project_root": str(tmp_path),
        "top_actions": [{
            "schema_version": "atomadic-forge.agent_action/v1",
            "id": "broken-card", "kind": "architectural",
            "title": "broken", "why": "",
            "risk": "low", "applyable": True,
            "write_scope": [],   # empty -> handler returns 'failed'
            "commands": [], "related_fcodes": ["F0042"],
            "next_command": "", "sample_path": None,
            "score_delta_estimate": 0,
        }],
        "action_count": 1, "applyable_count": 1, "next_command": "",
        "sources": {},
    }
    PlanStore(tmp_path).save_plan(plan)
    rep = apply_all_applyable(tmp_path, plan, apply=True)
    assert rep["halted_on"] == "failed"
    assert rep["applied_count"] == 0


