def test_cli_plan_step_dry_run_then_apply(tmp_path):
    pkg = _make_violation_tree(tmp_path)
    save = runner.invoke(app, ["plan", str(pkg), "--save", "--json"])
    plan = json.loads(save.stdout)
    plan_id = plan["id"]
    card_id = next(c["id"] for c in plan["top_actions"]
                    if c.get("applyable"))
    dry = runner.invoke(
        app, ["plan-step", plan_id, card_id,
              "--project", str(pkg), "--json"])
    assert dry.exit_code == 0
    assert json.loads(dry.stdout)["status"] == "dry_run"
    real = runner.invoke(
        app, ["plan-step", plan_id, card_id,
              "--project", str(pkg), "--apply", "--json"])
    assert real.exit_code == 0
    assert json.loads(real.stdout)["status"] == "applied"


