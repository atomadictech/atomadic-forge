def test_apply_card_F0042_routes_through_enforce(tmp_path):
    pkg = _make_violation_tree(tmp_path)
    plan = emit_agent_plan(
        project_root=str(pkg), goal="ship",
        wire_report={"schema_version": "atomadic-forge.wire/v1",
                      "verdict": "FAIL", "violation_count": 1,
                      "auto_fixable": 1,
                      "violations": [
                          {"file": "a1_at_functions/h.py",
                           "from_tier": "a1_at_functions",
                           "to_tier": "a3_og_features",
                           "imported": "F", "language": "python",
                           "f_code": "F0042", "proposed_fix": ""}]},
        package="pkg",
    )
    plan_id = PlanStore(pkg).save_plan(plan)
    plan["id"] = plan_id
    card_id = next(c["id"] for c in plan["top_actions"]
                    if "F0042" in (c.get("related_fcodes") or []))
    # Dry-run: file should NOT move.
    dry = apply_card(pkg, plan, card_id, apply=False)
    assert dry["status"] == "dry_run"
    assert (pkg / "a1_at_functions" / "h.py").exists()
    # Apply: enforce should move + reduce violations to 0.
    real = apply_card(pkg, plan, card_id, apply=True)
    assert real["status"] == "applied"
    assert real["detail"]["post_violations"] == 0
    assert (pkg / "a3_og_features" / "h.py").exists()
    assert not (pkg / "a1_at_functions" / "h.py").exists()
    # Outcome is in the plan-state file.
    assert PlanStore(pkg).card_status(plan_id, card_id) == "applied"


