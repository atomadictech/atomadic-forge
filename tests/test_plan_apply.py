def test_mcp_auto_step_dispatches(tmp_path):
    pkg = _make_violation_tree(tmp_path)
    plan = emit_agent_plan(
        project_root=str(pkg), goal="ship",
        wire_report={"schema_version": "atomadic-forge.wire/v1",
                      "verdict": "FAIL", "violation_count": 1,
                      "auto_fixable": 1,
                      "violations": [
                          {"file": "a1_at_functions/h.py",
                           "from_tier": "a1_at_functions",
                           "to_tier": "a3_og_features",
                           "imported": "F", "language": "python",
                           "f_code": "F0042", "proposed_fix": ""}]},
    )
    plan_id = PlanStore(pkg).save_plan(plan)
    card_id = next(c["id"] for c in plan["top_actions"] if c.get("applyable"))
    resp = dispatch_request(
        {"jsonrpc": "2.0", "id": 1, "method": "tools/call",
         "params": {"name": "auto_step",
                    "arguments": {"project": str(pkg),
                                   "plan_id": plan_id,
                                   "card_id": card_id,
                                   "apply": False}}},
        project_root=pkg,
    )
    body = json.loads(resp["result"]["content"][0]["text"])
    assert body["status"] == "dry_run"


